let day=parseInt(prompt("Enter a number (1 -7) for the day of the week"));
switch(day){
    case 1:
        console.log("Monday");
        break;
    case 2:
        console.log("Tuesday");
        break;
    case 3:
        console.log("Wednesday")
        break;
    case 4:
        console.log("Thursday")
        break;
    case 5:
        console.log("friday");
        break;
    default :
        console.log("Weekend or invalid day");
        break;

}